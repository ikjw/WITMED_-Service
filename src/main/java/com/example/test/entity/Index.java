package com.example.test.entity;

public class Index {
    private String id;
    private double icf;
    private double ecf;
    private double muscle;
    private double protein;
    private double ffweight;
    private double perfat;
    private double brate;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public double getIcf() {
        return icf;
    }

    public void setIcf(double icf) {
        this.icf = icf;
    }

    public double getEcf() {
        return ecf;
    }

    public void setEcf(double ecf) {
        this.ecf = ecf;
    }

    public double getMuscle() {
        return muscle;
    }

    public void setMuscle(double muscle) {
        this.muscle = muscle;
    }

    public double getProtein() {
        return protein;
    }

    public void setProtein(double protein) {
        this.protein = protein;
    }

    public double getFfweight() {
        return ffweight;
    }

    public void setFfweight(double ffweight) {
        this.ffweight = ffweight;
    }

    public double getPerfat() {
        return perfat;
    }

    public void setPerfat(double perfat) {
        this.perfat = perfat;
    }

    public double getBrate() {
        return brate;
    }

    public void setBrate(double brate) {
        this.brate = brate;
    }
}
